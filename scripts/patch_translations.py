def patch_po(path: Path, translations: dict) -> int:
    text = path.read_text(encoding="utf-8")
    filled = 0
    for msgid, msgstr in translations.items():
        # Escape the msgid for regex
        safe_id = re.escape(msgid)
        # Match msgid "..." followed by newline msgstr ""
        pattern = r'(msgid "' + safe_id + r'"\r?\nmsgstr )""'
        replacement = r'\1"' + msgstr.replace("\\", "\\\\").replace('"', '\\"') + '"'
        new_text, n = re.subn(pattern, replacement, text)
        if n:
            text = new_text
            filled += n
    path.write_text(text, encoding="utf-8")
    return filled


def compile_mo(po_path: Path) -> None:
    from babel.messages.pofile import read_po
    from babel.messages.mofile import write_mo
    mo_path = po_path.with_suffix(".mo")
    with open(str(po_path), encoding="utf-8") as f:
        catalog = read_po(f)
    with open(str(mo_path), "wb") as f:
        write_mo(f, catalog)
    print(f"  compiled -> {mo_path.name}  ({mo_path.stat().st_size} bytes)")


